    function getLang() {
        return localStorage.getItem('zodovia_lang') || 'si';
    }

    function applyTranslations(lang) {
        var strings = T[lang] || T['si'];

        document.querySelectorAll('[data-i18n]').forEach(function (el) {
            var key = el.getAttribute('data-i18n');
            if (strings[key] !== undefined) el.textContent = strings[key];
        });

        document.querySelectorAll('[data-i18n-html]').forEach(function (el) {
            var key = el.getAttribute('data-i18n-html');
            if (strings[key] !== undefined) el.innerHTML = strings[key];
        });

        document.querySelectorAll('[data-i18n-placeholder]').forEach(function (el) {
            var key = el.getAttribute('data-i18n-placeholder');
            if (strings[key] !== undefined) el.placeholder = strings[key];
        });

        document.documentElement.lang = lang;

        if (lang === 'si') {
            document.body.classList.add('lang-si');
        } else {
            document.body.classList.remove('lang-si');
        }

        document.querySelectorAll('.lang-toggle').forEach(function (btn) {
            btn.textContent = lang === 'si' ? 'EN' : 'සිං';
            btn.setAttribute('title', lang === 'si' ? 'Switch to English' : 'සිංහලට මාරු වන්න');
        });
    }

    window.setLang = function (lang) {
        localStorage.setItem('zodovia_lang', lang);
        applyTranslations(lang);
    };

    window.toggleLang = function () {
        window.setLang(getLang() === 'si' ? 'en' : 'si');
    };

    /* Expose t() for JavaScript files to look up translated strings */
    window.t = function (key) {
        var lang = getLang();
        var strings = T[lang] || T['si'];
        return (strings[key] !== undefined) ? strings[key] : ((T['en'][key] !== undefined) ? T['en'][key] : key);
    };

    window.ZODOVIA_LANG = getLang;

    document.addEventListener('DOMContentLoaded', function () {
        applyTranslations(getLang());
    });
